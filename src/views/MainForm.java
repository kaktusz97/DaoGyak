    private class EmployeeEventListener implements EntityEventListener<Employee> {

        @Override
        public void entityAdded(Employee t) {
            employees.add(t);
            refreshTable();
        }

        @Override
        public void entityUpdated(Employee t) {
            update(t);
            refreshTable();
        }

        @Override
        public void entityDeleted(Employee t) {
            employees.remove(t);
            refreshTable();
        }

        private void refreshTable() {
            employeeTableModel.fireTableDataChanged();
        }

        private void update(Employee e) {
            int i = 0;
            boolean found = false;
            while (i < employees.size() && !found) {
                if (employees.get(i).
                        getId().
                        equals(e.getId())) {
                    employees.set(i, e);
                    found = true;
                }
                i++;
            }
        }
    }
