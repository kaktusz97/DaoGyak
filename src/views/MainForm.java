    private void btUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btUpdateActionPerformed
        if (isRowSelected()) {
            int i = tblEmployee.getSelectedRow();
            EmployeeEventListener l = new EmployeeEventListener();
            EmployeeEditorForm form = new EmployeeEditorForm(this, employees.get(i));
            form.addEventListener(l);
            form.setVisible(true);
        }
    }//GEN-LAST:event_btUpdateActionPerformed

    private void btDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btDeleteActionPerformed
        if (isRowSelected()) {
            try {
                int i = tblEmployee.getSelectedRow();
                employeeService.delete(employees.get(i).getId());
                employees.remove(i);
                employeeTableModel.fireTableDataChanged();
            } catch (PersistenceException ex) {
                JOptionPane.showMessageDialog(this, "Delete error");
            }
        }
    }//GEN-LAST:event_btDeleteActionPerformed

    private void initEmployeeList() {
        try {
            employees = employeeService.getAllEntities();
        } catch (PersistenceException ex) {
            JOptionPane.showMessageDialog(this, "Cannot load employees");
        }
    }

    private void initCeosList() {
        try {
            ceos = ceoService.getAllEntities();
        } catch (PersistenceException ex) {
            JOptionPane.showMessageDialog(this, "Cannot load employees");
        }
    }

    private boolean isRowSelected() {
        return tblEmployee.getSelectedRow() >= 0;
    }

